  public static byte[] getHashIV(int length) {
    byte[] iv = new byte[length];

    System.arraycopy(CONSTANTS, 0, iv, 0, Math.min(length, CONSTANTS.length));

    for (int i = CONSTANTS.length; i < length; i++) {
      iv[i] = (byte) (CONSTANTS[i % CONSTANTS.length]
              * CONSTANTS[(i + 64 * (i / CONSTANTS.length) + (8 - i)) % CONSTANTS.length]);
    }

    return iv;
  }

  public static byte[] genIV(int blockSize) {
    byte[] iv = new byte[blockSize];
    new SecureRandom().nextBytes(iv);

    return iv;
  }
