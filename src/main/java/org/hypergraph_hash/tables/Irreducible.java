package org.hypergraph_hash.tables;

public class Irreducible {
  private Irreducible() {}

  private static final int[] GF8_IRREDUCIBLE = {
          0x11B, 0x11D, 0x12B, 0x12D, 0x139, 0x13F, 0x14D, 0x15F, 0x163, 0x165,
          0x169, 0x171, 0x177, 0x17B, 0x187, 0x18B, 0x18D, 0x19F, 0x1A3, 0x1A9,
          0x1B1, 0x1BD, 0x1C3, 0x1CF, 0x1D7, 0x1DD, 0x1E7, 0x1F3, 0x1F5, 0x1F9
  };

  private static final int[] GF8_PRIMITIVE = {
          0x11D, 0x12B, 0x12D, 0x14D, 0x15F, 0x163, 0x165, 0x169,
          0x171, 0x187, 0x18D, 0x1A9, 0x1C3, 0x1CF, 0x1E7, 0x1F5
  };

  public static int getGF8(int index) {
    return GF8_IRREDUCIBLE[index];
  }

  public static int getGF8Primitive(int index) {
    return GF8_PRIMITIVE[index];
  }
}
